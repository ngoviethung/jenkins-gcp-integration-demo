    public function groupleveltopic() {
        return $this->belongsTo(GroupLevelTopic::class,'group_level_topic_id');
    }

    public function types() {
        return $this->belongsToMany(Type::class, 'topic_types', 'topic_id', 'type_id')->withPivot('type_id', 'factor');
    }

    public function getListTypesAttribute(){
        $types = $this->types;
        $html = '';
        foreach ($types as $type) {
            $html .= "{$type->name}: {$type->pivot->factor} <br/>";
        }
        return $html;
    }

    public function country_models() {
        return $this->belongsToMany(Country::class, 'topic_country', 'topic_id', 'country_id');
    }

    public function versions() {
        return $this->hasMany(TopicRevision::class);
    }

    public function lastVersion() {
        return $this->versions()->orderByDesc('version')->first();
    }

