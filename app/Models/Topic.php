    public function setLocaleAttribute($value){

        $data = [];

        if($value){
            $value = json_decode($value);
            foreach ($value as $symbol => $val){
                if($val != ''){
                    $symbol = str_replace('name_', '', $symbol);
                    $data[$symbol] = $val;
                }
            }

        }
        if($data){
            $this->attributes['locale'] = json_encode($data);
        }else{
            $this->attributes['locale'] = NULL;
        }


    }
    public function getLocaleAttribute($value){

        if (Request::is('api*')) {
            return $value;
        }

        $haystack = url()->current();
        $needle = "admin/topic";

        if (strpos($haystack, $needle) !== false) {
            $data = [];
            if($value){
                $value = json_decode($value);
                foreach ($value as $symbol => $val){
                    $symbol = 'name_'.$symbol;
                    $data[$symbol] = $val;
                }
            }
            return json_encode($data);
        } else {
            return $value;
        }

    }
