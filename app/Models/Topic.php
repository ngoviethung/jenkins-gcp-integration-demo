    public function getLocale(){
        $str = '';
        $data = $this->locale;
        $data = json_decode($data);

        if($data){
            foreach ($data as $value) {
                $str .= $value . '<br>';
            }
        }
        $str = rtrim($str, "<br>");
        return $str;
    }

